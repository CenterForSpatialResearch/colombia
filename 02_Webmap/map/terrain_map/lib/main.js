function getLineWeight(c) {
    return c > 40000  ? 6 :
           c > 30000  ? 5 :
           c > 20000  ? 4 :
           c > 10000  ? 3 :
           c > 5000   ? 2 :
           c > 2500   ? 0.7 :
           c > 1000   ? 0.4 :
                        0.2;
};









  




